function setbluebackground() {
    let element = document.getElementById("style-text");
    element.style.backgroundColor = "blue";
}
function setwhitebackground() {
    document.getElementById("style-text").style.backgroundColor = "white";
}
function setredbackground() {
document.getElementById("style-text").classList.add("red-color");
}
function setremovebackground() {
    document.getElementById("style-text").classList.remove("red-color");

}